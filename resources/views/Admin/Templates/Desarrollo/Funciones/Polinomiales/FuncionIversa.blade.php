                                            <p style="text-align: center;">
                                            <center><font size="4"><b>Ejemplo 1</b></font>
                                                <br><br>
                                                Encontrar la función inversa de <b>f(x) = 3x + 2</b>
                                                <br><br>
                                                Primero sustituimos <b>f(x)</b> por <b>y</b>
                                                <br><br>
                                                <font size="3">y = 3x + 2</font>
                                                <br><br>
                                                Despejamos el valor de <b>x</b>
                                                <br><br>
                                                <font size="3">y - 2 = 3x</font>
                                                <br><br>
                                                <table border="0" style="text-align: center;">
                                                    <tr>
                                                        <td style="border-bottom: 1px solid grey"><font size="3">&nbsp;y - 2&nbsp;<font></td>
                                                        <td rowspan="2"><font size="3">&nbsp;&nbsp;= x&nbsp;&nbsp;</font></td>
                                                    </tr>
                                                    <tr>
                                                        <td><font size="3">3</font></td>
                                                    </tr>
                                                </table>
                                                <br><br>
                                                Es decir, que la función inversa de <b>f(x) = 3x + 2</b> es igual a :
                                                <br><br>
                                                <table border="0" style="text-align: center;">
                                                    <tr>
                                                        <td rowspan="2"><font size="3">&nbsp;&nbsp;x = &nbsp;&nbsp;</font></td>
