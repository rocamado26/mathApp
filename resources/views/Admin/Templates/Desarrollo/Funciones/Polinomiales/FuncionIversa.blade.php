                                                <br><br>
                                                Despejamos el valor de <b>x</b>
                                                <br><br>
                                                <font size="3">y + 2 = 5x</font>
                                                    <br><br>
                                                    <table border="0" style="text-align: center;">
                                                        <tr>
                                                            <td style="border-bottom: 1px solid grey"><font size="3">&nbsp;y + 2&nbsp;<font></td>
