                                            <strong>Funciones Inversas</strong><br><br>
                                            <p style="text-align: justify;">
                                                Funciones inversas, en el sentido más amplio, son funciones que hacen lo "contrario" de cada una. Por ejemplo, si <u>f</u> convierte <u>a</u> en <u>b</u>,
                                                entonces la inversa debe convertir <u>b</u> en <u>a</u>.
                                                <br><br>
                                                <center>
                                                <img src="img/iconos/funciones/polinomiales/inversa/teoria/inversa.png" style="width: 50%">
                                                </center>
                                                <br><br>
                                                O, en otras palabras: <font size="4"><b>f(a) = b ⇔ f(b) = a</b></font>
                                            </p>
