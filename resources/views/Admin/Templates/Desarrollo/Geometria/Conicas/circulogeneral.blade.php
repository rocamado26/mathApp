                                        <img src="img/iconos/geometria/conicas/circunferencia/teoria/teoriaCircunferencia.png" style="width: 70% !important;">
                                    </center>
                                    <br><br>
                                    Observa que en este caso, su centro C (3, 4) no coincide con el origen O(0, 0)
                                    <br><br>
                                    Como en el caso de la forma canónica de la circunferencia, vamos a calcular la distancia CP = r, donde C es su centro y P(x, y)
                                    es un punto variable de ella. Tienes:
                                    <br><br>
                                    √ (x - 3)<sup>2</sup> + (y - 4)<sup>2</sup> = 5
                                    <br><br>
                                    ( √ (x - 3)<sup>2</sup> + (y - 4)<sup>2</sup> )<sup>2</sup> = (5)<sup>2</sup> ; Elevando al cuadrado y efectuando
                                    <br><br>
                                    (x - 3)<sup>2</sup> + (y - 4)<sup>2</sup> = 5<sup>2</sup> ; Efectuamos el cuadrado del radio
                                    <br><br>
                                    (x - 3)<sup>2</sup> + (y - 4)<sup>2</sup> = 25
                                    <br><br>
                                    Por lo tanto la ecuación ordinaria de la circunferencia es:
                                    <br><br>
                                    <b>(x - 3)<sup>2</sup> + (y - 4)<sup>2</sup> = 25</b>
                                    <br><br>
                                    Ahora bien, si deseamos conocer la ecuación general de esta ecuacion ordinaria, debemos aplicarel binomio cuadrado perfecto para los valores
                                    de la derecha de la ecuación, es decir:
                                    <br><br>
                                    <b>x<sup>2</sup> - 6x + 9 + y<sup>2</sup> - 8y + 16 = 25</b> ; operando enteros
                                    <br><br>
                                    <b>x<sup>2</sup> + y<sup>2</sup> - 6x - 8y + 25 = 25</b> ; transponiendo 25
                                    <br><br>
                                    <b>x<sup>2</sup> + y<sup>2</sup> - 6x - 8y + 25 - 25 = 0</b> ; reduciendo términos semejantes
                                    <br><br>
                                    <b>x<sup>2</sup> + y<sup>2</sup> - 6x - 8y = 0</b> ; Ecuación general de la circunferencia
