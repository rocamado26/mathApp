                                    <br><br>
                                    ¿Cómo es la distancia que va desde el centro de la
                                    circunferencia a cualquiera de sus puntos?
                                    <br><br>
                                    ¿Cómo se le llama a esa distancia?
                                    <br><br>
                                    Recordarás que esa distancia es constante y se llama
                                    radio, al cual denotas por r. En este caso, <b>r = 3</b>
                                    <br><br>
                                    Como el centro del circulo es el origen de el plano y  r = 3, entonces, al aplicar la fórmula de la
                                    distancia entre dos puntos O(0, 0) y P(x, y), tienes:
                                    <br><br>
                                    √ (x - 0)<sup>2</sup> + (y - 0)<sup>2</sup> = 3
                                    <br><br>
                                    Ya que <b>x - 0 = x</b> y <b>y - 0 = y</b>
                                    <br><br>
                                    √ x<sup>2</sup> + y<sup>2</sup> = 3
                                    <br><br>
                                    ( √ x<sup>2</sup> + y<sup>2</sup> )<sup>2</sup> = (3)<sup>2</sup> ; Elevando al cuadrado y efectuando
                                    tienes
                                    <br><br>
                                    <b>x<sup>2</sup> + y<sup>2</sup> = 9</b>
                                    <br><br>
                                    Esta expresión se llama ecuación canónica de la
                                    circunferencia con r = 3
                                    <br><br>
                                    En general, si una circunferencia de radio r tiene su
                                    centro en O(0, 0), su ecuación se llama canónica, y se
                                    obtiene a partir de la distancia entre dos puntos (0, 0);
                                    (x, y) para lo cual obtienes:
                                    <br><br>
                                    <b>r = √ ( x - 0 )<sup>2</sup> ( y - 0 )<sup>2</sup></b>    La distancia es r
                                    <br><br>
                                    <b>r = √ x<sup>2</sup> + y<sup>2</sup></b>                  Efectuando las restas
                                    <br><br>
                                    <b>r<sup>2</sup> = x<sup>2</sup> +  y<sup>2</sup></b>       Elevando al cuadrado
                                    <br><br>
                                    <b>x<sup>2</sup> + y<sup>2</sup> = r<sup>2</sup></b>        Ecuación Canónica de la Circunferencia
                                    </p>
