                                                                <strong>Elementos de una elipse</strong><br><br>
                                                                <b>* Focos (F y F')</b>: Puntos fijos en los que la diferencia de distancia entre ellos y cualquier punto de la hipérbola es siempre la misma.
                                                                <br>
                                                                <b>* Eje focal, principal o real</b>: Recta que pasa por los focos.
                                                                <br>
                                                                <b>* Eje secundario o imaginario</b>: Mediatriz del segmento que une los dos focos.
                                                                <br>
                                                                <b>* Centro (O)</b>: Punto de intersección de los ejes focal y secundario.
                                                                <br>
                                                                <b>* Semidistancia focal (c)</b>: La mitad de la distancia entre los dos focos F y F'. Su valor es c.
                                                                <br>
                                                                <b>* Distancia focal (2c)</b>: Distancia del segmento que une los dos focos F y F'. Su longitud es 2c.
                                                                <br>
                                                                <b>* Los vértices (A y A')</b>:  Puntos de la hipérbola que cortan al eje focal.
