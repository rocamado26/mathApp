                                                               <strong>Elementos de una elipse</strong><br><br>
                                                               <b>* Centro</b>: Es el punto de intersección de los ejes. Es, además, centro de simetría.
                                                               <br>
                                                               <b>* Eje principal o focal</b>: Es el eje en el que se encuentran los focos. Es un eje de simetría.
                                                               <br>
                                                               <b>* Eje secundario</b>: Es el eje perpendicular al eje principal, mediatríz del segmento que une los focos.
                                                               <br>
                                                               <b>* Vértices</b>: Puntos de intersección de la elipse con los ejes.
                                                               <br>
                                                               <b>* Distancia focal</b>: Distancia entre los focos. Su longitud es 2·c.
                                                               <br>
                                                               <b>* Semidistancia focal</b>: Distancia entre el centro y cada foco. Su longitud es c.
                                                               <br>
                                                               <b>* Semieje mayor o principal</b>: Segmento entre el centro y los vértices del eje principal.
                                                               Su longitud es a.
                                                               <br>
                                                               <b>* Semieje menor o secundario</b>: Segmento entre el centro y los vértices del eje secundario.
                                                               Su longitud es b
                                                           </div>
