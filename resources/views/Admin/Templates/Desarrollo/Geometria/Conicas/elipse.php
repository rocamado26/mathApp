                                                   <br><br>
                                                   En otras palabras, se trata de una circunferencia achatada que se caracteriza porque la suma de
                                                   las distancias desde cualquiera de sus puntos <b>P</b> hasta otros dos puntos denominados focos <b>(F y F')</b> es siempre la misma.
