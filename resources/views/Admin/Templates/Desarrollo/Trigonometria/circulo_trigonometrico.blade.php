                                            <strong>Círculo trigonométrico</strong><br><br>
                                            <p style="text-align: justify;">
                                                También conocido como <b>goniométrico</b>, es aquel círculo cuyo centro coincide con el origen de coordenadas del plano cartesiano y cuyo radio mide la unidad. El círculo trigonométrico tiene la ventaja de ser una herramienta práctica en el manejo de los conceptos de trigonometría, pero al mismo tiempo es un apoyo teórico, pues ayuda a fundamentar y tener una idea precisa y formal de las funciones trigonométricas. <br><br> 
                                                <center><img src="{{asset('img/iconos/trigonometria/areadesarrollo/circulo-trigo.png')}}"></center><br><br>
                                                Atreves del círculo trigonométrico se puede obtener de forma manual o analítica el valor aproximado de las razones trigonométricas para un ángulo determinado si se dispone de los instrumentos geométricos necesarios. <br><br>

                                                En el círculo trigonométrico, las funciones de ángulos son más sencillas de manejar y obtener, debido a que el valor “<b>d</b>” es el radio de la circunferencia y es igual a 1. Así <b>d = r = 1</b> <br><br>
