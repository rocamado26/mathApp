                                                    <b>Las funciones trigonométricas las definimos así:</b>
                                                    <br><br>
                                                    Función <b>seno ()</b> es la relación del cateto opuesto al ángulo entre la hipotenusa.
                                                    <br><br>
                                                    Función <b>coseno ()</b> es la relación del cateto adyacente al ángulo entre la hipotenusa. <br><br>
                                                    Función <b>tangente ()</b> es la relación del cateto opuesto al ángulo entre el cateto adyacente. <br><br>
                                                    Función <b>cosecante ()</b> es la relación de la hipotenusa  entre el cateto opuesto al ángulo.  <br><br>
                                                    Función <b>secante ()</b> es la relación de la hipotenusa  entre el cateto adyacente al ángulo.  <br><br>
                                                    Función <b>cotangente ()</b> es la relación entre el cateto adyacente entre el cateto opuesto al ángulo.
