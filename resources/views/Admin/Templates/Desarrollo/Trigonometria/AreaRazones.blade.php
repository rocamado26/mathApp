                                                <p style="text-align: justify;"> <b></b><br>
                                                Una manera sencilla de recordar las razones trigonométricas es utilizando la palabra sohcahtoa, nos ayuda a recordar las definiciones de seno, coseno y tangente. Estas letras representan las iniciales de lo fórmula para cada una de las razones. <br><br>
                                                <ul>
                                                    <li><b>SOH =</b> nos representa a la razón Seno que es igual a Opuesto sobre la Hipotenusa. <br></li>
                                                    <li><b>CAH =</b> nos representa la razón <b>C</b>oseno que es igual a <b>A</b>dyacente sobre la <b>H</b>ipotenusa. <br></li>
                                                    <li><b>TOA =</b> nos representa la razón Tangente que es igual a Adyacente sobre Opuesto . <br></li>
