/**
 * [anguloCalculado Calcula el ángulo complemento apartir de uno.]
 * @param  {[int]} id [identificador de que ángulo es.]
 * @return {[]}    [Sin retorno]
 */
function anguloCalculado(id){
    if (id=='txtaa') {
        if ($('#txtaa').val()!=0 && $('#txtaa').val()>=0) {
            if ($('#txtaa').val()<90) {
                $('#txtab').val(90-$('#txtaa').val());
                anterior=$('#txtaa').val();
                $('#txtab').removeAttr("disabled");
                $('#chab').removeAttr("checked");
            } else {
                toastr.error('El valor del ángulo debe ser menor a 90 grados.');
                $('#txtaa').val(anterior);
            }
        } else {
            $('#txtab').val('');
        }
    } else {
        if ($('#txtab').val()!=0 && $('#txtab').val()>=0) {
            if ($('#txtab').val()<90) {
                $('#txtaa').val(90-$('#txtab').val()); 
                anterior=$('#txtab').val();
                $('#txtaa').removeAttr("disabled");
                $('#chaa').removeAttr("checked");   
            } else {
                toastr.error('El valor del ángulo debe ser menor a 90 grados.');
                $('#txtab').val(anterior);
            }   
        } else {
            $('#txtaa').val('');
        }
    }
}

