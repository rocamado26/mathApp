/**
 * [ImprimeRespuesta Realiza la operación para llegar a un valor numérico en caso de ser posible. de lo contrario genera una expresión.]
 * @param {[numeric]} v1 [Valor 1 a operar.]
 * @param {[numeric]} v2 [Valor 2 a operar.]
 * @param {[int]} op [Proceso a realizar]
 * @param {[string]} vx [Variable a buscar]
 * @param {[int]} ft [Función trigonométrica seleccionada.]
 */
function ImprimeRespuesta(v1,v2,op,vx,ft){
    var resultado=0;
    var funcion=0;

    if (validar_num(v1) && validar_num(v2)) {
        switch(op){
            case 1:{
                funcion=OperacionFunciones(v2,ft);
                resultado=v1/funcion;
            }break;
            case 2:{
                funcion=OperacionFunciones(v2,ft);
                resultado=v1*funcion;
            }break;
            case 3:{
                resultado=v1/v2;
                resultado=AnguloFunciones(resultado,ft);
            }break;
            case 4:{
                resultado=v1/v2;
            }break;
        }
        $("#Respuesta").html('<label>'+vx+' = '+Math.round(resultado * 100) / 100+'</label>');
    } else {
        $("#Respuesta").html('<label> No es posible llegar a un valor numérico para '+vx+'.</label>');
    }
}
/**Valida si un si el parametro num es número.
 * @param  {[var]} num [número a validar]
 * @return {[boolean]} [true si es número]
 */
function validar_num(num){
    if(num!=''){
        return true;
    }else{
        return false;
    }
}
/**
 * [OperacionFunciones Opera las funciones trigonométricas y devuelve su valor]
 * @param {[numeric]} angulo [Ángulo de análisis]
 * @param {[int]} op     [Función trigonométrica seleccionada]
 */
function OperacionFunciones(angulo,op) {
    switch(op){
        case 1:{return Math.sin(radianes(angulo));}break;
        case 2:{return Math.cos(radianes(angulo));}break;
        case 3:{return Math.tan(radianes(angulo));}break;
        case 4:{return 1/Math.sin(radianes(angulo));}break;
        case 5:{return 1/Math.cos(radianes(angulo));}break;
        case 6:{return 1/Math.tan(radianes(angulo));}break;
    }
}
/**
 * [AnguloFunciones Operación para determinar el valor del ángulo.]
 * @param {[numeric]} valor [Valor resultante de la división de los lados involucrados.]
 * @param {[int]} op     [Función trigonométrica seleccionada]
 */
function AnguloFunciones(valor,op) {
    switch(op){
        case 1:{return grados(Math.asin(valor));}break;
        case 2:{return grados(Math.acos(valor));}break;
        case 3:{return grados(Math.atan(valor));}break;
    }
}

/** Convertir de grados a radianes.
 * @param  {[numeric] grados [Grados a convertir]}
 * @return {[numeric]  [Valor en radianes]}
 */
function radianes(grados) {
    return grados * Math.PI / 180;
}
 
/**
 * [grados Convertir de radianes a grados.]
 * @param  {[numeric]} radians [Valor en radianes]
 * @return {[numeric]}         [Valor en grados]
 */
function grados(radians) {
    return radians * 180 / Math.PI;
}

