 * [vericaPractica Comprueba si la práctica se resolvió de forma correcta]
 * @return {[type]} [description]
 */
function vericaPractica(){
    var bueno=0,malo=0;
    for (var i = 0; i <= 5; i++) {
        if (vP[i]==1) {
            limpiaPractica(i+5,1);
            bueno++;
        } else {
            limpiaPractica(i+5,0);
            malo++;
        }
    }
    if(bueno==6){
