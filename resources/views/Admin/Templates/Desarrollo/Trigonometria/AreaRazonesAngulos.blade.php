								resuelveParte2(VerificaValor('ladoPA'),VerificaValor('ladoPB'),'cot (45°)');
							} else {
								$('#Resultado').html('cot (60°) = '+VerificaValor('ladoPA')+' / '+VerificaValor('ladoPB'));
								resuelveParte2(VerificaValor('ladoPA'),VerificaValor('ladoPB'),'cot (60°)');
							}
						}
					} else {$('#Resultado').html('<center><img src="img/iconos/generales/cancel-bold.png">&nbsp;&nbsp;</center>');
							$('#Resultado').append('Has hecho una mala elección, revisa la teoría del tema.');
							toastr.error('Revisa los datos ingresados.','Error');$('#Total').html('');}
				}break;
	}
}

